class Switch {

    public static void main(String args[])
    {
        int n = 2;
    switch(n)
    {
        case 1 :
        {
            System.out.println("heheheh");
        }
        break;
        case 2 :
        {
            System.out.println("byeeeee");
            
        }
        break;

        default:
        {
            System.out.println("default");
        }
    }

    }
    
}
